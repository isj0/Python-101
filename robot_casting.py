# Type Casting

speed_str = '30'
print(speed_str)
# Check the data type
print(type(speed_str))

# Casting to int
speed_int = int(speed_str)
print(speed_int)
# Check the data type
print(type(speed_int))

# Casting to float
speed_float = float(speed_str)
print(speed_float)
# Check the data type
print(type(speed_float))

distance_float =  120.5
print(distance_float)
# Check the data type
print(type(distance_float))

distance_int = int(distance_float)
print(distance_int)
# Check the data type
print(type(distance_int))

